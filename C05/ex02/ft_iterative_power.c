int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	r;

	r = 1;
	i = 1;
	if (power < 0)
	{
		return (0);
	}
	if (power == 0)
	{
		return (1);
	}
	while (i <= power)
	{
		r *= nb;
		i++;
	}
	return (r);
}
