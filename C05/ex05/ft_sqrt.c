int	ft_sqrt(int nb)
{
	int	k;
	int	r;

	k = 1;
	while (k > 0)
	{
		r = k * k;
		if (r == nb)
			return (k);
		if (r > nb)
			return (0);
		k++;
	}
	return (k);
}
